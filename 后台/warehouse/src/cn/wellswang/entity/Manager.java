package cn.wellswang.entity;

public class Manager {
	private Integer man_id;
	private String man_name;
	private String man_password;
	private Integer man_type;
	public Integer getMan_id() {
		return man_id;
	}
	public void setMan_id(Integer man_id) {
		this.man_id = man_id;
	}
	public String getMan_name() {
		return man_name;
	}
	public void setMan_name(String man_name) {
		this.man_name = man_name;
	}
	public String getMan_password() {
		return man_password;
	}
	public void setMan_password(String man_password) {
		this.man_password = man_password;
	}
	public Integer getMan_type() {
		return man_type;
	}
	public void setMan_type(Integer man_type) {
		this.man_type = man_type;
	}
}
